/**
 * - var:
 *    + global scope
 *    + có thể gán lại giá trị
 *    + tạo một thuộc tính toàn cục trên window (window.variable)
 */

/**
 * - let:
 *    + block scope
 *    + có thể gán lại giá trị nhưng không thể khai báo lại
 *    + không tạo thuộc tính toàn cục trên window như var
 */

/**
 * - const:
 *    + block scope
 *    + không thể khai báo & gán lại biến
 *    + khai báo phải có 1 giá trị ban đầu
 *    + không tạo thuộc tính toàn cục trên window như var
 */